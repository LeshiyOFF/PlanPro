package com.projectlibre1.ui;

/**
 * Port for UI services - isolates GUI from Core
 * Follows Hexagonal Architecture principles (Ports & Adapters)
 */
public interface UIServicePort {
    
    // File operations
    String chooseFileName(boolean save, String selectedFileName);
    boolean isFileNameAllowed(String fileName, boolean save);
    String getFileExtension(String fileName);
    String changeFileExtension(String fileName, String extension);
    int getFileType(String fileName);
    String getFileExtensionByType(int fileType);

    // Notifications
    void warn(Object message);
    void error(Object message);
    int confirm(Object message, int optionType); // optionType: YES_NO, YES_NO_CANCEL, etc.
}
